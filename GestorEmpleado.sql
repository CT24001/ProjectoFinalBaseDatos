--  DDL for View VISTA_EMPLEADOS_OPERATIVA
--------------------------------------------------------

  CREATE OR REPLACE FORCE EDITIONABLE VIEW "VISTA_EMPLEADOS_OPERATIVA" ("EMPLEADO_ID", "NOMBRE_COMPLETO", "EMAIL", "TELEFONO", "FECHA_CONTRATACION", "SALARIO_FORMATEADO", "COMISION_PCT", "TITULO_PUESTO", "NOMBRE_DEPARTAMENTO", "NOMBRE_JEFE", "CIUDAD", "PROVINCIA", "NOMBRE_PAIS", "NOMBRE_REGION", "ANIOS_ANTIGUEDAD") AS 
  SELECT 
    e.empleado_id,
    e.nombre || ' ' || e.apellido AS nombre_completo,      -- Nombre completo concatenado
    e.email,
    e.telefono,
    TO_CHAR(e.fecha_contratacion, 'DD/MM/YYYY') AS fecha_contratacion,  -- Fecha formateada
    TO_CHAR(e.salario, 'FM$999,999.99') AS salario_formateado,          -- Salario con formato moneda
    e.comision_pct,
    p.titulo_puesto,                                      -- Título del puesto desde tabla Puesto
    d.nombre_departamento,                                -- Departamento del empleado
    jefe.nombre || ' ' || jefe.apellido AS nombre_jefe,   -- Nombre del jefe (LEFT JOIN permite nulos)
    l.ciudad,                                             -- Ubicación geográfica
    l.provincia,
    pa.nombre_pais,
    r.nombre_region,
    ROUND((SYSDATE - e.fecha_contratacion) / 365, 1) AS anios_antiguedad  -- Años trabajados
FROM adminproyecto.empleado e
INNER JOIN adminproyecto.puesto p ON e.puesto_id = p.puesto_id
INNER JOIN adminproyecto.departamento d ON e.departamento_id = d.departamento_id
LEFT JOIN adminproyecto.empleado jefe ON e.manager_id = jefe.empleado_id    -- LEFT JOIN porque CEO no tiene jefe
LEFT JOIN adminproyecto.localizacion l ON d.localizacion_id = l.localizacion_id
LEFT JOIN adminproyecto.pais pa ON l.pais_id = pa.pais_id
LEFT JOIN adminproyecto.region r ON pa.region_id = r.region_id
;
  GRANT SELECT ON "VISTA_EMPLEADOS_OPERATIVA" TO "USR_LECTURA";
