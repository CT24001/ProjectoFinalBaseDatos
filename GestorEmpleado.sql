    IF v_monto_inactivos > 0 THEN
        v_variacion_pct := ((v_monto_activos - v_monto_inactivos) / v_monto_inactivos) * 100;
    ELSE
        v_variacion_pct := 100.00; -- Crecimiento neto absoluto si el histórico inactivo es cero
